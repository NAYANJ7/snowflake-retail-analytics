CREATE OR REPLACE VIEW ANALYTICS.VW_SHIP_MODE_ANALYSIS AS
SELECT
    SHIP_MODE,
    COUNT(*)                                AS TOTAL_LINE_ITEMS,
    ROUND(AVG(DELIVERY_DAYS), 1)            AS AVG_DELIVERY_DAYS,
    MIN(DELIVERY_DAYS)                      AS MIN_DELIVERY_DAYS,
    MAX(DELIVERY_DAYS)                      AS MAX_DELIVERY_DAYS,
    ROUND(STDDEV(DELIVERY_DAYS), 2)         AS DELIVERY_STD_DEV,
    SUM(NET_PRICE)                          AS TOTAL_REVENUE,
    COUNT(CASE WHEN IS_RETURNED THEN 1 END) AS TOTAL_RETURNS,
    COUNT(CASE WHEN IS_ON_TIME THEN 1 END)  AS ON_TIME_COUNT,
    ROUND(COUNT(CASE WHEN IS_ON_TIME THEN 1 END) / COUNT(*) * 100, 2) AS ON_TIME_PCT
FROM MARTS.FACT_LINEITEM
GROUP BY 1
ORDER BY TOTAL_REVENUE DESC;